		[TestCase(SAMPLE, 26397)]
		[TestCase(INPUT, 271245)]
		public void Puzzle1(string input, int expected)
		{

			var lines = input.Split(Environment.NewLine).ToList();

			// valid opening chars are (, <, {, [

			var openingChars = new char[] {'(', '[', '{', '<'};
			var closingChars = new char[] {')', ']', '}', '>'};
			var scores = new int[] {3, 57, 1197, 25137};

			var score = 0;
			foreach (var line in lines)
			{
				var parity = 0;
				var stack = new Stack<char>();
				for (var i = 0; i < line.Length; i++)
				{
					var c = line[i];
					var isOpener = openingChars.Contains(c);
					if (isOpener)
					{
						stack.Push(c);
						continue;
					}

					var charIndex = Array.IndexOf(closingChars, c);

					var isCloser = charIndex >= 0;
					if (isCloser)
					{
						var expectedOpeningChar = openingChars[charIndex];
						var actualOpeningChar = stack.Pop();

						if (expectedOpeningChar != actualOpeningChar)
						{
							score += scores[charIndex];
							break;
						}
					}

				}
			}

			Assert.AreEqual(expected, score);

		}

		[TestCase(SAMPLE, 288957)]
		[TestCase(INPUT, 1685293086)]
		public void Puzzle2(string input, long expected)
		{
			var lines = input.Split(Environment.NewLine).ToList();

			// valid opening chars are (, <, {, [

			var openingChars = new char[] {'(', '[', '{', '<'};
			var closingChars = new char[] {')', ']', '}', '>'};
			var scores = new int[] {1, 2, 3, 4};

			var lineScores = new List<long>();
			foreach (var line in lines)
			{
				var score = 0L;

				var stack = new Stack<char>();
				var corrupt = false;
				for (var i = 0; i < line.Length; i++)
				{
					var c = line[i];
					var isOpener = openingChars.Contains(c);
					if (isOpener)
					{
						stack.Push(c);
						continue;
					}

					var charIndex = Array.IndexOf(closingChars, c);

					var isCloser = charIndex >= 0;
					if (isCloser)
					{
						var expectedOpeningChar = openingChars[charIndex];
						var actualOpeningChar = stack.Pop();

						if (expectedOpeningChar != actualOpeningChar)
						{
							corrupt = true;
							break;
						}
					}
				}

				if (!corrupt && stack.Count > 0)
				{
					// found an incomplete line!
					// we need to go back down the stack adding the values to the string, and accumulating score
					while (stack.Count > 0)
					{
						var fix = stack.Pop();
						var fixIndex = Array.IndexOf(openingChars, fix);
						score *= 5;
						score += scores[fixIndex];
					}
					lineScores.Add(score);
				}
			}

			lineScores.Sort();
			var middleScore = lineScores[lineScores.Count / 2]; // 3 / 2 = 1, 7 / 2 = 3
			Assert.AreEqual(expected, middleScore);

			// TOO LOW: 169394671

		}


